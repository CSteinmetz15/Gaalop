package de.gaalop.algebra;

/**
 * Stores a defined algebra by ID and name
 * @author Christian Steinmetz
 */
public class DefinedAlgebra {
    
    public String id;
    public String name;

    public DefinedAlgebra(String id, String name) {
        this.id = id;
        this.name = name;
    }
}
